    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Sala s = new Sala();
        SalaDao sala = null;
        
        try {
            sala = new SalaDao();
            if (!txtSala.getText().isEmpty() && !txtAndar.getText().isEmpty() && !txtLocal.getText().isEmpty()) {
                s.setSala(txtSala.getText());
                s.setAndar(txtAndar.getText());
                s.setLocal(txtLocal.getText());
                
                sala.create(s);
            }else {
                JOptionPane.showMessageDialog(null, "Os campos n√£o podem estar em branco");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

